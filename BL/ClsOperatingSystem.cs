using Domains;
using Microsoft.EntityFrameworkCore;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BL
{
    public interface OperatingSystemService
    {
        List<TbOperatingSystem> getAll();
        bool Add(TbOperatingSystem client);
        bool Edit(TbOperatingSystem client);
        bool Delete(TbOperatingSystem client);


    }
    public class ClsOperatingSystem : OperatingSystemService
    {
        AlAl3abDbContext ctx;

        public ClsOperatingSystem(AlAl3abDbContext context)
        {
            ctx = context;
        }
        public List<TbOperatingSystem> getAll()
        {
            //_4ZsoftwareCompanyTestTaskContext o_4ZsoftwareCompanyTestTaskContext = new _4ZsoftwareCompanyTestTaskContext();
            List<TbOperatingSystem> lstOperatingSystems = ctx.TbOperatingSystems.ToList();

            return lstOperatingSystems;
        }

        public bool Add(TbOperatingSystem item)
        {
            try
            {
                //_4ZsoftwareCompanyTestTaskContext o_4ZsoftwareCompanyTestTaskContext = new _4ZsoftwareCompanyTestTaskContext();
                item.CurrentState = 1;
                ctx.TbOperatingSystems.Add(item);
                ctx.SaveChanges();

                return true;
            }
            catch (Exception ex)
            {
                return false;

            }
        }
        public bool Edit(TbOperatingSystem item)
        {
            try
            {
                //_4ZsoftwareCompanyTestTaskContext o_4ZsoftwareCompanyTestTaskContext = new _4ZsoftwareCompanyTestTaskContext();

                ctx.Entry(item).State = EntityState.Modified;
                ctx.SaveChanges();
                return true;
            }
            catch (Exception ex)
            {
                return false;

            }
        }

        public bool Delete(TbOperatingSystem item)
        {
            try
            {
                //_4ZsoftwareCompanyTestTaskContext o_4ZsoftwareCompanyTestTaskContext = new _4ZsoftwareCompanyTestTaskContext();

                ctx.Entry(item).State = EntityState.Deleted;
                ctx.SaveChanges();
                return true;
            }
            catch (Exception ex)
            {
                return false;

            }
        }
    }
}
